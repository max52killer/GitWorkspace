//修改数据表格的loadFilter，用于将非标准格式的数据转换为DataGrid需要的格式的数据
$.fn.datagrid.defaults.loadFilter=function(ret) {
	if(ret.success) {
		return ret.data;
	}
	
	$.messager.alert("提示",ret.msg ? ret.msg : "服务端出错！","error");
}

/**
 * 
 * 
 * @requires jQuery,EasyUI
 * 
 * 通用错误提示
 * 
 * 用于datagrid/treegrid/tree/combogrid/combobox/form加载数据出错时的操作
 */

var easyuiErrorFunction = function(XMLHttpRequest) {
	try {
	   $.messager.progress('close');
	}catch(e) {
		
	}
	
	try {
		var resp = $.parseJSON(XMLHttpRequest.responseText);
		$.messager.alert('错误', resp.msg,"error");
	}catch(e) {
		$.messager.alert('错误', XMLHttpRequest.responseText,"error");
	}
	

	//$.messager.alert('错误', '系统内部错误!');
};

$.fn.datagrid.defaults.onLoadError = easyuiErrorFunction;
$.fn.treegrid.defaults.onLoadError = easyuiErrorFunction;
$.fn.tree.defaults.onLoadError = easyuiErrorFunction;
$.fn.combogrid.defaults.onLoadError = easyuiErrorFunction;
$.fn.combobox.defaults.onLoadError = easyuiErrorFunction;
$.fn.form.defaults.onLoadError = easyuiErrorFunction;


$.ajaxSetup({
	cache : false,
	dataType : "json",
	error : easyuiErrorFunction,
	type : "POST"	
});



/**
 *  
 * @requires jQuery,EasyUI
 * 
 * 扩展tree，使其支持平滑数据格式
 */
$.fn.tree.defaults.loadFilter = function(ret, parent) {
	if(!ret.success) {
		$.messager.alert("提示",ret.msg);
		return;
	}
	
	var data = ret.data;
	var opt = $(this).data().tree.options;
	var idFiled, textFiled, parentField;
	if (opt.parentField) {
		idFiled = opt.idFiled || 'id';
		textFiled = opt.textFiled || 'text';
		parentField = opt.parentField;
		var i, l, treeData = [], tmpMap = [];
		for (i = 0, l = data.length; i < l; i++) {
			tmpMap[data[i][idFiled]] = data[i];
		}
		for (i = 0, l = data.length; i < l; i++) {
			if (tmpMap[data[i][parentField]] && data[i][idFiled] != data[i][parentField]) {
				if (!tmpMap[data[i][parentField]]['children'])
					tmpMap[data[i][parentField]]['children'] = [];
				data[i]['text'] = data[i][textFiled];
				tmpMap[data[i][parentField]]['children'].push(data[i]);
			} else {
				data[i]['text'] = data[i][textFiled];
				treeData.push(data[i]);
			}
		}
		return treeData;
	}
	return data;
};

